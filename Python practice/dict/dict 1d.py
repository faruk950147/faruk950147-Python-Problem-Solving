# 1D Dictionary

products = {
    "id": 1,
    "title": "Ipad M-10",
    "price": 100000,
    "description": "Ipad M-10 is a great product",
    "status": 200
}
print(products)
print(products['title'])
print(products['price'])
print(products['description'])
print(products['status'])
print(type(products))
print(type(products['title']))
print(type(products['price']))
print(type(products['description']))
print(type(products['status']))
