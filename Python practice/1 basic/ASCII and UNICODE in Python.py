# ASCII and UNICODE in Python
# ASCII - American Standard Code for Information Interchange 1
# Capital letter A to Z = 65 to 90
# Small letter a to z = 97 to 122
# ord() function returns the Unicode code point of a character
# chr() function returns the character that represents the Unicode code point   
print(ord("A"))
print(ord("a"))
print(ord("0"))
print(ord(" "))

print(chr(65))
print(chr(97))
print(chr(48))
print(chr(32))

