# == equal
# != not equal
# > greater than
# < less than
# >= greater than or equal to
# <= less than or equal to

a = 10
b = 20
print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)

if a == b:
    print("Equal")
else:
    print("Not Equal")

if a != b:
    print("Not Equal")
else:
    print("Equal")

if a > b:
    print("Greater")
else:
    print("Not Greater")

if a < b:
    print("Less")
else:
    print("Not Less")

if a >= b:
    print("Greater or Equal")
else:
    print("Not Greater or Equal")

if a <= b:
    print("Less or Equal")
else:
    print("Not Less or Equal")